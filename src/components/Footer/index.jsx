function Footer() {
  const canvasRef = useRef(null);
  const [hovered, setHovered] = useState(null);

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext('2d');
    let animId;
    const resize = () => { canvas.width = canvas.offsetWidth; canvas.height = canvas.offsetHeight; };
    resize();
    window.addEventListener('resize', resize);
    const particles = Array.from({ length: 45 }, () => ({
      x: Math.random() * canvas.width, y: Math.random() * canvas.height,
      r: Math.random() * 1.6 + 0.4,
      dx: (Math.random() - 0.5) * 0.3, dy: (Math.random() - 0.5) * 0.3,
      o: Math.random() * 0.35 + 0.08,
    }));
    const draw = () => {
      ctx.clearRect(0, 0, canvas.width, canvas.height);
      particles.forEach(p => {
        ctx.beginPath(); ctx.arc(p.x, p.y, p.r, 0, Math.PI * 2);
        ctx.fillStyle = `rgba(255,255,255,${p.o})`; ctx.fill();
        p.x += p.dx; p.y += p.dy;
        if (p.x < 0 || p.x > canvas.width) p.dx *= -1;
        if (p.y < 0 || p.y > canvas.height) p.dy *= -1;
      });
      animId = requestAnimationFrame(draw);
    };
    draw();
    return () => { cancelAnimationFrame(animId); window.removeEventListener('resize', resize); };
  }, []);

  return (
    <footer className="footer">
      <canvas ref={canvasRef} className="footer-canvas" aria-hidden="true" />
      <div className="footer-inner">
        <div className="footer-top">
          <div className="footer-brand">
            <div className="footer-logo-mark"><img src="/l.png" alt="Logo" /></div>
            <div>
              <p className="footer-brand-name">Nibir Abeer</p>
              <p className="footer-brand-sub">Frontend &amp; Full-Stack Developer</p>
            </div>
          </div>

          <nav className="footer-nav">
            {NAV_LINKS.map(({ label, path }) => (
              <Link key={label} to={path} className="footer-nav-link">{label}</Link>
            ))}
          </nav>

          <div className="footer-socials">
            {SOCIAL_LINKS.map(s => (
              <a key={s.label} href={s.href} className="footer-social-btn"
                aria-label={s.label} target="_blank" rel="noreferrer"
                onMouseEnter={() => setHovered(s.label)} onMouseLeave={() => setHovered(null)}>
                <span className="footer-social-icon">{s.icon}</span>
                <span className={`footer-social-label ${hovered === s.label ? 'visible' : ''}`}>{s.label}</span>
              </a>
            ))}
          </div>
        </div>

        <div className="footer-divider" />

        <div className="footer-bottom">
          <p className="footer-copy">© {new Date().getFullYear()} Nibir Abeer. All rights reserved.</p>
          <div className="footer-status">
            <span className="footer-dot" />
            <span>Open to work — let's build something great</span>
          </div>
          <p className="footer-made">Made with <span className="footer-heart">♥</span> using React</p>
        </div>
      </div>
    </footer>
  );
}

export default Footer;